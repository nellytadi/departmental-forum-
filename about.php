                    <h3>History of NACOSS</h3><p>
  The Nigerian Association of Computer Science Students (NACOSS)  is a student professional body with presence in almost all tertiary institutions in Nigeria (both private and government owned). NACOSS was founded by groups of students in July 1993 with the backing of Nigerian Computer Society (NCS) as its parent body.      

It provides avenues for students (in any IT related field) to highlight and champion issues of interest in a coordinated and organized manner. NACOSS members (NACOSSites) are students studying in tertiary institutions in computer related disciplines including: Computer Science, Computer Engineering, Information Technology, etc. Each member is essentially a member of the Association’s local chapter in his or her institution.

As of April 2012, NACOSS states that it is the largest and most organized student body in West Africa, with registered members of about Two Hundred and Fifty thousand (250 000) in well over One Hundred and fifty local chapters (and more unregistered) in different Institutions (Universities, Polytechnics and Colleges of Education) across the six (6) Geo-political zones of Nigeria.
</p>
